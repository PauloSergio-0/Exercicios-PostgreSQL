with 
calculo_aluguel as (
	select
		f.rental_duration as limite_posse,
		r.return_date::date - r.rental_date::date as posse_filme,
		case 
			when (r.return_date::date - r.rental_date::date) > f.rental_duration then 'atrasado'
			else 'regular'
		end status_aluguel,
		r.*
	from rental r 
	join inventory i on i.inventory_id = r.inventory_id
	join film f on f.film_id = i.film_id
),
clientes_aluguel as (select 
	c.customer_id as "ID Cliente",
	concat(c.first_name ,' ' , c.last_name) as "Nome Cliente",
	count(*) filter(where cal.status_aluguel = 'atrasado') as "Total Atrasado", --uso filtro na contagem para contas apenas os atrasados
	count(*) as "Total Aluguel"
from customer c
	join calculo_aluguel cal on cal.customer_id = c.customer_id
	group by c.customer_id,c.first_name  , c.last_name
)
select 
	c.*,
	concat( round( (c."Total Atrasado"::numeric / c."Total Aluguel") * 100, 2 ) , '%') as "Taxa retorno atrasado"
from clientes_aluguel c
order by ((c."Total Atrasado"::numeric / c."Total Aluguel") * 100) desc

